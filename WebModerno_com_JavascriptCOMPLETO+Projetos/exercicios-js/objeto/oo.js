/** Princípios importantes
 * 1. Abstração = pegar um objeto do mundo real e saber traduzi-lo e simplificalo para o programa e adequando as suas necessidades
 * 2. Encapsulamento = detalhes de implementação escondidos, mostrando apenas o que é necessário para ser usado
 * 3. Herança (prototype) = reuso de código. Utilizar composições menores dentro de códigos mais complexos. Receber características, atributos e comportamentos de um objeto 'pai'. Em JS, cada extrutura só pode receber um 'pai' e esse pai pode ter outro, o que forma uma cadeia.
 * 4. Polimorfismo = multiplas formas. Havendo uma variável mais genérica, e nela podem ser referênciadas  coisas que fazem parte da herança do mais genérico (subtipos).
 */